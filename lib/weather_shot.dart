class Sys {
  final int type;
  final int id;
  final String country;
  final int sunrise;
  final int sunset;

  Sys({
    required this.type,
    required this.id,
    required this.country,
    required this.sunrise,
    required this.sunset,
  });

  factory Sys.fromJson(Map<String, dynamic> json) {
    return Sys(
      type: json['type'],
      id: json['id'],
      country: json['country'],
      sunrise: json['sunrise'],
      sunset: json['sunset'],
    );
  }
}

class WeatherLoader {
  final String city;
  final Uri uri;

  WeatherLoader({required String city})
      : city = city.toLowerCase().trim(),
        uri = Uri.parse(
            "https://api.openweathermap.org/data/2.5/weather?q=$city&appid=7ea5bee46986f62402c03be21e187948");

  Future<WeatherResponse> fetchWeather() async {
    final response = await http.get(uri);

    if (response.statusCode == 200) {
      return WeatherResponse.fromJson(
          jsonDecode(response.body) as Map<String, dynamic>);
    } else {
      throw Exception('Failed to load weather with the reason ${response.reasonPhrase}');
    }
  }
}
